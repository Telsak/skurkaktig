class Dwarf:
    def __init__(self, subrace: str) -> None:
        self._subrace = subrace
        self._name = f'{subrace} Dwarf'
        if subrace == 'Hill':
            self._stats = {'wis':1}
        elif subrace == 'Mountain':
            self._stats = {'str':2}
        self._size = 'Medium'

    def __str__(self) -> str:
        return self._name
